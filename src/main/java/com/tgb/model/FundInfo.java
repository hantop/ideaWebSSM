package com.tgb.model;

public class FundInfo {


    int fund_id;
    String fund_code;
    String fund_name;
    String fund_manager;
    String fund_new_price;
    String fund_new_rate;

    public int getFund_id() {
        return fund_id;
    }

    public void setFund_id(int fund_id) {
        this.fund_id = fund_id;
    }

    public String getFund_code() {
        return fund_code;
    }

    public void setFund_code(String fund_code) {
        this.fund_code = fund_code;
    }

    public String getFund_name() {
        return fund_name;
    }

    public void setFund_name(String fund_name) {
        this.fund_name = fund_name;
    }

    public String getFund_manager() {
        return fund_manager;
    }

    public void setFund_manager(String fund_manager) {
        this.fund_manager = fund_manager;
    }

    public String getFund_new_price() {
        return fund_new_price;
    }

    public void setFund_new_price(String fund_new_price) {
        this.fund_new_price = fund_new_price;
    }

    public String getFund_new_rate() {
        return fund_new_rate;
    }

    public void setFund_new_rate(String fund_new_rate) {
        this.fund_new_rate = fund_new_rate;
    }
}
